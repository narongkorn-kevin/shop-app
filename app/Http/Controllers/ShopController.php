    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $name = $request->name;
        $detail = $request->detail;
        $qty = $request->qty;
        $price = $request->price;
        $exd = $request->exd;

        $shop = new Shop();
        $shop->name = $name;
        $shop->detail = $detail;
        $shop->qty = $qty;
        $shop->price = $price;
        $shop->exd = $exd;
        $shop->save();

        return response()->json([
            'code' => strval(200),
            'status' => true,
            'message' => 'เรียกดู',
            'data' => $shop,
        ], 200);

    }

